package mvc.modeldto;

public class lectureDTO {
	private int s_id;
	private String sub_name;
	private String lec_score;
	private int lec_no_date;
	private int lec_year;
	private int lec_semester;
	
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}
	public String getLec_score() {
		return lec_score;
	}
	public void setLec_score(String lec_score) {
		this.lec_score = lec_score;
	}
	public int getLec_no_date() {
		return lec_no_date;
	}
	public void setLec_no_date(int lec_no_date) {
		this.lec_no_date = lec_no_date;
	}
	public int getLec_year() {
		return lec_year;
	}
	public void setLec_year(int lec_year) {
		this.lec_year = lec_year;
	}
	public int getLec_semester() {
		return lec_semester;
	}
	public void setLec_semester(int lec_semester) {
		this.lec_semester = lec_semester;
	}
	
	
}
